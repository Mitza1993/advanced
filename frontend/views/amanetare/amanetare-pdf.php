<P CLASS="western" STYLE="margin-bottom: 0in">   <B>CAP. IV.
DREPTURILE SI OBLIGATIILE CREDITORULUI</B></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000000"> 
 </FONT><FONT COLOR="#000000"><B>Art. 3. </B></FONT><FONT COLOR="#000000">Creditorul
se obliga sa restituie debitorului bunurile primite cu titlu de
amanet la data cand debitorul isi indeplineste obligatia de plata a
datoriei.</FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000000"> 
 </FONT><FONT COLOR="#000000"><B>Art. 4. </B></FONT><FONT COLOR="#000000">Creditorul
asigura depozitarea, conservarea, manipularea si paza bunurilor
primite de la debitor si raspunde pentru degradarea, distrugerea ori
disparitia din culpa sa a acestora, pana la concurenta sumei
prevazute la art. 1. Cazurilor de forta majora si caz fortuit
exonereaza de raspundere pe creditor.</FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000000"> 
 </FONT><FONT COLOR="#000000"><B>Art. 5. </B></FONT><FONT COLOR="#000000">Creditorul
are obligatia sa nu utilizeze in folosul sau bunurile primite de la
debitor cu titlu de amanet.</FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000000"> 
 </FONT><FONT COLOR="#000000"><B>Art. 6. </B></FONT><FONT COLOR="#000000">Creditorul
este indreptatit a fi platit cu preferinta din bunurile amanetate,
inaintea altor creditori ai debitorului din prezentul contract, cu
indeplinirea formalitatilor legale in materie, valoarea de referinta
fiind cea stabilita de parti.</FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
