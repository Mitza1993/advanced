incheierea prezentului contract.</FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000000"> 
 </FONT><FONT COLOR="#000000"><B>Art. 9. </B></FONT><FONT COLOR="#000000">Debitorul
